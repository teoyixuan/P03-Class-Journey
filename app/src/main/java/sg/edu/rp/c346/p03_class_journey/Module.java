package sg.edu.rp.c346.p03_class_journey;

public class Module {
    private String name;
    private String code;

    public Module(String name, String code) {
        this.name = name;
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }
}
